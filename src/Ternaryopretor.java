public class Ternaryopretor
{
    public static void main(String args[])
    {
        //Instead of If-else Statement We Can Go With TernaryOperator
        int num1=10,num2=20;
        int res=(num1>num2)? (num1+num2):(num1-num2);
        System.out.println(res);


        int num3=5,num4=10;
        int res1=(num3>num4)? num3:num4;
        System.out.println(res1);
    }
}
